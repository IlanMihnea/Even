/**
 * Downloads Font Awesome 6 Free SVGs from jsDelivr and generates
 * project/css/icons.css using mask-image (same HTML/JS, zero markup changes).
 */

const https = require('https');
const fs = require('fs');
const path = require('path');

const FA_VERSION = '6.5.0';
const BASE = `https://cdn.jsdelivr.net/npm/@fortawesome/fontawesome-free@${FA_VERSION}/svgs`;

// All icons needed by public pages + JS (admin/sign/brochure excluded)
const SOLID = [
  'arrow-right', 'arrow-up-right-from-square', 'bars', 'bed', 'bolt',
  'calendar-check', 'cart-shopping', 'check', 'chevron-down', 'chevron-left',
  'chevron-right', 'chevron-up', 'circle-exclamation', 'clock', 'droplet',
  'envelope', 'expand', 'file-lines', 'file-pdf', 'fire', 'folder-open',
  'heart', 'location-dot', 'magnifying-glass', 'map', 'map-location-dot',
  'object-group', 'paper-plane', 'phone', 'ruler-combined', 'school',
  'share-nodes', 'sliders', 'spinner', 'stamp', 'toilet', 'train-subway',
  'tree', 'vector-square', 'xmark',
];
const REGULAR = ['heart', 'clock'];
const BRANDS  = ['whatsapp'];

function fetch(url) {
  return new Promise((resolve, reject) => {
    https.get(url, res => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        return fetch(res.headers.location).then(resolve, reject);
      }
      if (res.statusCode !== 200) return reject(new Error(`HTTP ${res.statusCode} for ${url}`));
      let data = '';
      res.on('data', c => data += c);
      res.on('end', () => resolve(data));
    }).on('error', reject);
  });
}

function svgToDataUri(svg) {
  // Minimal encoding: remove newlines, encode special chars for CSS url()
  const clean = svg
    .replace(/\n/g, ' ')
    .replace(/<!--.*?-->/g, '')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/"/g, "'");
  return `"data:image/svg+xml,${encodeURIComponent(clean)}"`;
}

function extractViewBox(svg) {
  const m = svg.match(/viewBox="([^"]+)"/);
  return m ? m[1] : '0 0 512 512';
}

async function fetchSvg(variant, name) {
  const url = `${BASE}/${variant}/${name}.svg`;
  try {
    const svg = await fetch(url);
    return { name, variant, svg };
  } catch (e) {
    console.warn(`  WARN: could not fetch ${url}: ${e.message}`);
    return null;
  }
}

(async () => {
  const tasks = [
    ...SOLID.map(n => fetchSvg('solid', n)),
    ...REGULAR.map(n => fetchSvg('regular', n)),
    ...BRANDS.map(n => fetchSvg('brands', n)),
  ];
  const results = (await Promise.all(tasks)).filter(Boolean);
  console.log(`Fetched ${results.length} icons`);

  let css = `/* Auto-generated by scripts/gen-icons-css.js — do not edit manually */
/* Font Awesome 6 Free icons as CSS mask-image. Zero HTML/JS changes required. */

.fa-solid, .fa-regular, .fa-brands {
  display: inline-block;
  width: 1em;
  height: 1em;
  vertical-align: -0.125em;
  background: currentColor;
  -webkit-mask-repeat: no-repeat;
  -webkit-mask-position: center;
  -webkit-mask-size: contain;
  mask-repeat: no-repeat;
  mask-position: center;
  mask-size: contain;
}

/* fa-spin */
@keyframes fa-spin { from { transform: rotate(0deg); } to { transform: rotate(360deg); } }
.fa-spin { animation: fa-spin 2s linear infinite; }

`;

  for (const { name, variant, svg } of results) {
    const dataUri = svgToDataUri(svg);
    const selector = `.fa-${variant}.fa-${name}`;
    css += `${selector} {\n  -webkit-mask-image: url(${dataUri});\n  mask-image: url(${dataUri});\n}\n`;
  }

  const out = path.join(__dirname, '../project/css/icons.css');
  fs.writeFileSync(out, css, 'utf8');
  const kb = Math.round(fs.statSync(out).size / 1024);
  console.log(`Written: project/css/icons.css (${kb} KB)`);
})();
